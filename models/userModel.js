/** @format */

const mongoose = require('mongoose');

const registerSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  cpassword: {
    type: String,
    required: true,
  },
});

const userModel = mongoose.model('register', registerSchema);

module.exports = userModel;
